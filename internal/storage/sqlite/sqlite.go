package sqlite

import (
	"database/sql"
	"fmt"

	_ "github.com/mattn/go-sqlite3"
)

type Storage struct {
	db *sql.DB
}

func New(storagePath string) (*Storage, error) {
	const op = "storage.sqlite.New"

	db, err := sql.Open("sqlite3", storagePath)
	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}
	defer db.Close()

	// готовим запрос
	stmt, err := db.Prepare(`CREATE TABLE IF NOT EXISTS data(
		id INTEGER PRIMARY KEY,
		loaded TEXT NOT NULL UNIQUE);
	CREATE INDEX IF NOT EXISTS idx_alias ON data(loaded);
	)`)
	if err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	if _, err := stmt.Exec(); err != nil {
		return nil, fmt.Errorf("%s: %w", op, err)
	}

	return &Storage{db: db}, nil
}
